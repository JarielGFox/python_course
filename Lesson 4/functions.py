# Functions in python

# Una funzione è un blocco di codice che viene eseguito quando viene chiamata. Usiamo le funzioni per evitare di ripeterci e di riscrivere codice ripetitivo che possiamo riutilizzare. 

# Le funzioni possono avere parametri, che sono variabili che vengono passate alla funzione.

# Per scrivere una funzione usiamo def()

def greetings(nome):
    print('Benvenuto alla lezione di Python, ' + nome)
    
# Una volta che abbiamo costruito la funzione, dobbiamo invocarla, altrimenti non funzionerò e dobbiamo passare un argomento che andrà a posizionarsi all'interno della funzione dove abbiamo dichiarato il parametro, in questo caso, nome.

greetings('Giovanni')

# Ricordiamo che le funzioni possono servire per raggruppare codice complesso e riutilizzarlo dovunque vogliamo, semplicemente richiamando la funzione e passando argomenti differenti a seconda delle necessità.

 